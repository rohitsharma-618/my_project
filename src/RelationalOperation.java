public class RelationalOperation  {
    public static void main(String[] args) {
        int P = 100, Q = 20;

        System.out.println(P == Q);
        System.out.println(P != Q);
        System.out.println(P <= Q);
        System.out.println(P >= Q);
        System.out.println(P > Q);
        System.out.println(P < Q);

    }
}
